package pt.utl.ist.fenix.tools.file.filters;

public class NullPointerFileSetFilterException extends FileSetFilterException {

    public NullPointerFileSetFilterException() {
        super();
    }

    public NullPointerFileSetFilterException(String message) {
        super(message);
    }

    public NullPointerFileSetFilterException(String message, Throwable cause) {
        super(message, cause);
    }

    public NullPointerFileSetFilterException(Throwable cause) {
        super(cause);
    }

}
